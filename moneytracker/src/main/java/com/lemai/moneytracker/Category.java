package com.lemai.moneytracker;

/**
 *
 * @author Mai
 */
public class Category {
    private String name;
    private String type; // "Income" or "Expense"

    public Category(String name, String type) {
        this.name = name;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }
}

